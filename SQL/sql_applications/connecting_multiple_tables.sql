SELECT U.USERNAME_,
U.GENDER,U.EMAIL , 
A.ADDRESSTEXT,
C.COUNTRY ,
CT.CITY ,
T.TOWN,
A.POSTACODE,
D.TOWNID
FROM 
USERS U , 
ADRESS A , 
COUNTRIES C ,
CITIES CT ,
DISTRICTS D ,
TOWN T 
WHERE U.ID= A.USERID
AND U.ID=1
AND C.ID = A.COUNTRYID
AND CT.ID = A.CITYID
AND T.ID = A.CITYID
AND D.ID= A.TOWNID
--AND CT.CITY LIKE 'BURDUR'
ORDER BY U.USERNAME_

